package com.example.praktikum_pbo.p6.praktikum.percobaan.part1.percobaan3;

import lombok.Setter;

@Setter
public class Tabung extends Bangun {
    protected int t;

    public void setSuperPhi(double phi) {
        super.phi = phi;
    }

    public void setSuperR(int r){
        super.r = r;
    }

    public void volume() {
        System.out.println("Volume tabung adalah: " + (phi * r * t));
    }
}